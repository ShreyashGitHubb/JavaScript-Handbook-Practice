// write a java script program to find wheather a number is divisible by 2 and 3

let num = 6;

// the === will strictly check the number with its type
if (num % 2 === 0 && num % 3 === 0) {  
    console.log(`the number ${num} is divisible by 2 and 3 `);
}
else {
    console.log(`the number ${num} is not divisible by 2 and 3`);
}

